def valuesize2mainlist(value,size):
	"A function that taking the value and size of a molecule returns the primary structure"
	mainstructurelist=[0]*size
	offset=size-1
	while value:
		mainstructurelist[offset]=value&1
		value=value>>1
		offset-=1
	return mainstructurelist
